package day11;

public class Greetings {
	
	
	//1) No param No return value
	
     void m1()
     
     {
    	 
    	 System.out.println("Welcome to Java");
     }
     
     //2) No param  returns value
     
     String m2() {
    	 
    	 return(" How are you doing");
     }

 
     //3) Takes Param  But wont return value
     
     void m3(String designation) {
    	 
    	 System.out.println("Hello"  + designation);
    	 
     }
     
     //4)Takes Param and return value
     
     String m4(String name){
    	 
    	 return(name);
    	 
     }

	

	

	

	

}
